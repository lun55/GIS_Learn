# 在IDE环境中，不可变数据类型共享引用,也就是不同的变量指向相同的对象
print("这是浮点型数字")
a = 100000000.0
b = 100000000.0
print(a is b)
print(id(a),id(b))

print("这是整数")
a = 100000000
b = 100000000
print(a is b)
print(id(a),id(b))


print("这是字符串")
a = "##################"
b = "##################"
print(a is b)
print(id(a),id(b))

print("这是元组")
a = 1,1,4,4,7,7.4
b = 1,1,4,4,7,7.4
print(a is b)
print(id(a),id(b))

# 可变数据类型时，不同变量指向不同的对象，即使对象的值相同，其内存地址并不相同

